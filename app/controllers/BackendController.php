	/**
	 * Store a newly created resource in storage.
	 *
	 * @return Response
	 */
	public function store()
	{
		$input = Input::all();
		$attempt = Auth::attempt([
			'username' => $input['username'], 
			'password' => $input['password']
			]);

		if ($attempt) return Redirect::to('uusbaas');	
		else
			return Redirect::to('uusbaas/login');
	}

