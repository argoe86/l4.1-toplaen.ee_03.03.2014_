<p>laenust. Eelkõige kasutab Laenuandja õigust leppetrahvile juhul, kui Laenusaaja ei ole Lepingust tulenevat</p>

<p>kohustust täitnud Laenuandja poolt antud täiendava tähtaja jooksul. Laenusaaja kohustub leppetrahvi</p>

<p>tasuma leppetrahvinõudes märgitud ulatuses ja tähtpäeval ning kõrvaldama rikkumise(-d), järgides</p>

<p>Laenuandja poolt antud tähtaegu ja juhiseid. Leppetrahvi maksmine ei asenda rikutud kohustuste ega</p>

<p>teiste Lepingust tulenevate kohustuste täitmist, samuti ei välista Laenuandja õigust Lepingu erakorraliseks ülesütlemiseks.</p>

