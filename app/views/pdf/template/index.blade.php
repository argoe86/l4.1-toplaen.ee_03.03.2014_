<p>9. Laenusaaja kinnitab, et Laenusaaja on õigus-, teo-, ja otsustusvõimeline isik Eesti Vabariigi seaduste alusel ning tal on piiramata õigused ja volitused Lepingu sõlmimiseks ja enesele Lepinguga võetud kohustuste täitmiseks.</p>

