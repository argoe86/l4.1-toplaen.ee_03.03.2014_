<p>8. <strong>Muud kulud. Laenu sissenõudmise menetluskulud.</strong></p>

<p>1. Laenusaaja kannab kõik kulud, mis on seotud Lepingu täitmisega, sh. kulud Lepingu järgsete maksete</p>

<p>tasumisega, samuti kohtukulud, mis on seotud Lepingust tulenevate vaidluste lahendamisega, kui Eesti</p>

<p>Vabariigi õigusaktidega ei ole ette nähtud teisiti.</p>

<p>2. Juhul, kui Laenusaaja rikub Lepingust tulenevaid kohustusi (s.t. Laenusaaja on hilinenud Lepinguliste</p>

<p>Maksete tasumisega Laenuandjale), on Laenuandjal õigus loovutada kõik Laenusaaja vastu suunatud</p>

<p>rahalised nõuded inkassoteenust osutavale ettevõttele ja/või võtta tarvitusele seadusega lubatud</p>

<p>meetmed võlgnevuse sissenõudmiseks.</p>

<p>3. Laenuandjal on õigus Laenusaajalt nõuda lisaks Laenule ja muudele Maksetele ka laenu sissenõudmisega seotud menetluskulusid. Menetluskulude miinimumsuurused on:</p>

<p>1. Laenu ja Maksete tähtaja ületamisel 7 või enam kalendripäeva, kuid vähem kui 21 kalendripäeva –</p>

<p>menetluskulud summas vähemalt 10 (kümme) eurot;</p>

<p>2. Laenu ja Maksete tähtaja ületamisel 21 või enam kalendripäeva, kuid vähem kui 35 kalendripäeva –</p>

<p>menetluskulud summas vähemalt 23 (kakskümmend kolm) eurot;</p>

<p>3. Laenu ja Maksete tähtaja ületamisel 35 või enam kalendripäeva, kuid vähem kui 65 kalendripäeva –</p>

<p>menetluskulud summas vähemalt 42 (nelikümmend kaks) euroti;</p>

<p>4. Laenu ja Maksete tähtaja ületamisel 65 või enam kalendripäeva lisandub menetluskulusid 42 eurot iga</p>

<p>täiendava 30 kalendripäeva kohta;</p>

<p>5. Menetluskulud hagiavalduse või maksekäsu kiirmenetluse avalduse esitamisel kohtusse (toimub kui</p>

<p>Laenusaaja Lepingust tulenevad kohustused on Laenuandja ees pikaajaliselt täitmata) summas</p>

<p>vähemalt 64 (kuuskümmend neli) eurot, lisaks veel õigusabikulud, riigilõiv ja muud kohtukulud.</p>

<p>4. Menetluskulu käesoleva Lepingu mõistes on tasu, mis katab võlanõude analüüsi kulu, pretensioonide</p>

<p>koostamise ja edastamise kulud, Laenuandja töötajate palgakulud jne.</p>

