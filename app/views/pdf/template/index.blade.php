<p>1. Oma elu- või töökoha muutumisest.</p>

<p>2. Laenusaaja pankrotimenetluse algatamisest.</p>

<p>3. Sündmustest, mis muul viisil oluliselt takistavad või teevad võimatuks Lepingu tingimuste täitmise</p>

<p>Laenusaaja poolt.</p>

