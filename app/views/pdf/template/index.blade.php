<p>9. <strong>Lepingu täitmise tagamine</strong></p>

<p>1. Laenusaaja vastutab Lepingust tulenevate kohustuste täitmise eest kogu oma olemasoleva ja tulevikus</p>

<p>omandatava varaga.</p>

<p>2. Laenusaaja vastutab täielikult ja tingimusteta kõigi tema poolt Laenuandjale esitatud dokumentide</p>

<p>õigsuse eest. Laenusaaja on kohustatud hüvitama Laenuandjale kõik kahjud, mis Laenuandja kannab</p>

<p>seoses Laenusaaja poolt väärinformatsiooni esitamise või tegeliku olukorra ja tähtsust omava</p>

<p>informatsiooni teadliku varjamisega.</p>

<p>3. Laenusaaja annab lepingu sõlmimisega laenuandjale tagasivõtmatu õiguse kontrollida Laenusaaja esitatud andmete tõepärasust ning tema senist maksekäitumist oma partneritelt, riiklikest registritest,</p>

<p>krediidiasutustest, maksehäireregistrist jms.</p>

<p>4. Laenuandja teostab laenutaotluse ja laenutaotleja maksevõime analüüsi. Laenuandjal on laenutaotluse ja laenutaotleja saaja maksevõime analüüsi teostamiseks ning Laenu väljastamisega seotud asjaolude</p>

<p>tuvastamiseks õigus nõuda laenutaotlejalt tema sissetulekut ja majanduslikku olukorda tõendavate</p>

<p>andmete ning dokumentide esitamist.</p>

<p>5. Laenusaaja annab laenuandjale käesoleva lepinguga tagasivõtmatu nõusoleku käesolevast lepingust</p>

<p>tulenevate mistahes rahaliste kohustuste täitmata jätmise korral oma isikuandmete (nimi ja isikukood)</p>

<p>ning maksehäire tekkimise kuupäeva ning summa edastamisega AS-le Krediidiinfo ning lubab loetletud</p>

<p>andmete töötlemist krediidiotsuste langetamise eesmärgil AS Krediidiinfo poolt hallatavas</p>

<p>maksehäireregistris (täpsem informatsioon <a href="http://www.krediidiinfo.ee/">www.krediidiinfo.ee</a>)</p>

