<p>10. <strong>Lepingu lõppemine</strong></p>

<p>1. Leping jõustub allakirjutamise hetkest ning lõpeb nõuetekohase täitmisega Laenusaaja ja/või kolmandate isikute poolt.</p>

<p>2. Laenusaajal on õigus lepingust taganeda 14 (neljateistkümne) päeva jooksul arvates selle sõlmimisest, esitades Laenuandjale selle aja jooksul kirjaliku avalduse ja tagastades laenusumma ning sellelt arvestatud intressid 14 (neljateistkümne) päeva jooksul arvates taganemise avalduse esitamisest. Nimetatud kohustuse mittetäitmisel loetakse, et Laenuandja ei ole lepingust taganenud.</p>

<p>3. Pooltel on õigus Leping erakorraliselt üles öelda Lepingus ja seaduses toodud alustel.</p>

