<p>esinemise tõttu ning laenuandja ei ole sundinud teda laenu võtma.</p>

<p>7. Laenusaaja kinnitab, et Laenuandja on selgitanud talle võimalust taotleda laenu Eesti Vabariigis</p>

<p>tegutsevatest pankadest või Laenuandjaga samaliigilist teenust osutavatest äriühingutest, aga Laenusaaja soovib sellele vaatamata käesoleva Lepingu sõlmimist.</p>

<p>8. Laenusaaja kinnitab, et on teadlik, et krediidiasutuste tarbimislaenude krediidi kulukuse määr võib olla</p>

