<p>11. <strong>Lepingu erakorraline ülesütlemine</strong></p>

<p>1. Pooltel on õigus Leping erakorraliselt üles öelda Lepingus ja seaduses toodud alustel.</p>

<p>2. Laenuandjal on õigus Leping üles öelda ja nõuda laenu kohest tagastamist, kui leiab aset kasvõi üks</p>

<p>alljärgnevalt loetletud sündmustest:</p>

<p>1. Laenusaaja on laenu taotlemisel või muudes laenuga seotud dokumentides andnud Laenuandjale</p>

<p>väärinformatsiooni või jätnud esitamata talle teadaolevad Lepingu täitmist mõjutavad andmed;</p>

<p>2. Laenusaaja ei ole nõuetekohaselt tasunud tähtaegselt ja/või ettenähtud summas kasvõi ühte Lepingu</p>

<p>alusel tasumisele kuuluvat makset;</p>

<p>3. Laenusaaja ei täida maksekohustusi mõne muu tema poolt võetud laenu osas ning see deklareeritakse</p>

<p>ennetähtaegselt tagasimaksmisele kuuluvaks;</p>

<p>4. Laenusaaja ei täida muid Lepingus ja/või selle täitmise tagamiseks sõlmitud lepingus sätestatud</p>

<p>tingimusi;</p>

<p>5. Ilmneb Laenusaaja võimetus täita Lepingut nõuetekohaselt.</p>

<p>3. Lepingu Laenuandja poolsel erakorralisel ülesütlemisel kohustub Laenusaaja tasuma kõik Laenuandjale</p>

<p>võlgnetavad summad 7 (seitsme) kalendripäeva jooksul arvates Lepingu erakorralise ülesütlemise teate</p>

<p>kättesaamist.</p>

<p>4. Laenusaajal on õigus 7 (seitsme) päevase kirjaliku etteteatamisega Leping üles öelda ja tagastada</p>

<p>Laenuandjale laenusumma ning tasuda intressid. Laenusumma ennetähtaegsel tagasimaksmisel toimub</p>

<p>intresside arvestus ühe kalendrikuu eest vastavalt lepingu p 3 ja Lisas 1 sätestatud määradele.</p>

