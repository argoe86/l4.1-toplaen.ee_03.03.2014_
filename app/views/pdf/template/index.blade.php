<p>2. Viivise arvestamisel lähtutakse tegelikust päevade arvust kuus ja 360-päevasest aastast.</p>

<p>3. Viivise arvestamine lõpetatakse võlgnevuse tasumise päeval ning tasumata viiviselt ja intressilt viivist ei arvestata.</p>

