<p>12. <strong>Lõppsätted</strong></p>

<p>1. Lepingut võib muuta Poolte kokkuleppel kirjalikus vormis või muus kirjalikus taasesitamist võimaldavas</p>

<p>vormis. Pooled ei välista lepingu muutmist ka muus (sh suulises) vormis.</p>

<p>2. Pooled lepivad kokku, et Lepingu tingimusteks ei loeta Poolte varasemaid tahteavaldusi, tegusid ega</p>

<p>kokkuleppeid, mis ei ole Lepingus otseselt sätestatud.</p>

<p>3. Käesoleva lepingu mistahes sätte võimalik vastuolu seadusega ei mõjuta ülejäänud lepingu sätete</p>

<p>kehtivust ega ole aluseks lepingu erakorraliseks ülesütlemiseks. Lepinguga reguleerimata küsimustes</p>

<p>juhindutakse Eesti Vabariigi seadusandlusest.</p>

<p>4. Pooled seavad eesmärgiks lahendada Lepingu täitmisel tekkivad vaidlused läbirääkimiste teel. Kokkuleppe mittesaavutamisel lahendatakse vaidlused Eesti Vabariigi seadustega ettenähtud korras kostja elukoha järgses Maakohtus või Pärnu Maakohtu Maksekäsuosakonnas.</p>

<p>5. Lepingu allkirjastamisega kinnitab laenuvõtja, et omab kindlat sissetulekuallikat (töökoht/pension) ning</p>

<p>garanteerib laenu tagastamise lepingus toodud tingimustel.</p>

<p>6. Leping on koostatud kahes identses võrdset juriidilist jõudu omavas eksemplaris, millest üks eksemplar</p>

<p>jääb Laenuandjale ja teine Laenusaajale.</p>

