<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">1. Kui Laenusaaja ei tee Lepingu alusel saadud Laenu ja/või muude Maksete tagasimakset tähtaegselt, siis on Laenuandjal õigus nõuda Laenusaajalt viivist Lepingus sätestatud Intressi määras. Viivist arvutatakse tähtaegselt tasumata summalt iga maksmisega viivitatud päeva eest, lähtuvalt viivise määrast ja maksmisega viivitatud päevade arvust.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">2. Viivise arvestamisel lähtutakse tegelikust päevade arvust kuus ja 360-päevasest aastast.</span></span></p>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">3. Viivise arvestamine lõpetatakse võlgnevuse tasumise päeval ning tasumata viiviselt ja intressilt viivist ei arvestata.</span></span></p>
<br />
<br />
 
<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><strong>7. Leppetrahv</strong></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">7.1. Kui Laenusaaja rikub ühte või mitut Lepingust või seadusest tulenevat kohustust Laenuandja ees, mis ei seisne selles, et Laenusaaja viivitab võlgnevate Maksete tasumisega, siis on Laenuandjal õigus nõuda Laenusaajalt leppetrahvi 20% (kahekümne protsendi) ulatuses kohustuse rikkumise hetkel tagastamata laenust. Eelkõige kasutab Laenuandja õigust leppetrahvile juhul, kui Laenusaaja ei ole Lepingust tulenevat kohustust täitnud Laenuandja poolt antud täiendava tähtaja jooksul. Laenusaaja kohustub leppetrahvi tasuma leppetrahvinõudes märgitud ulatuses ja tähtpäeval ning kõrvaldama rikkumise(-d), järgides Laenuandja poolt antud tähtaegu ja juhiseid. Leppetrahvi maksmine ei asenda rikutud kohustuste ega teiste Lepingust tulenevate kohustuste täitmist, samuti ei välista Laenuandja õigust Lepingu erakorraliseks ülesütlemiseks.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><strong>8. Muud kulud. Laenu sissenõudmise menetluskulud.</strong></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">8.1. Laenusaaja kannab kõik kulud, mis on seotud Lepingu täitmisega, sh kulud Lepingu järgsete maksete tasumisega, samuti kohtukulud, mis on seotud Lepingust tulenevate vaidluste lahendamisega, kui Eesti Vabariigi õigusaktidega ei ole ette nähtud teisiti.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">8.2. Juhul, kui Laenusaaja rikub Lepingust tulenevaid kohustusi (s.t. Laenusaaja on hilinenud Lepinguliste Maksete tasumisega Laenuandjale), on Laenuandjal õigus loovutada kõik Laenusaaja vastu suunatud rahalised nõuded inkassoteenust osutavale ettevõttele ja/või võtta tarvitusele seadusega lubatud meetmed võlgnevuse sissenõudmiseks.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">8.3. Inkassoettevõttel, kellele on Laenuandja poolt antud kõik vastavasisulised õigused ja volitused, on õigus koos Laenuandjaga või iseseisvalt nõuda lisaks Laenule ja muudele maksetele ka Laenu sissenõudmisega seotud menetluskulusid ja Laenusaaja kohustub hüvitama Laenuandjale ja ka inkassoettevõttele Laenusaaja poolt Lepingust tulenevate kohustuste rikkumisest või täitmata jätmisest tekkinud kulutused.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">8.4. Laenuandjal on õigus Laenusaajalt nõuda lisaks Laenule ja muudele Maksetele ka laenu sissenõudmisega seotud menetluskulusid. Menetluskulude miinimumsuurused on:</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">1) Laenu ja Maksete tähtaja ületamisel 7 või enam kalendripäeva, kuid vähem kui 21 kalendripäeva – menetluskulud summas vähemalt 10 (kümme) eurot;</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">2) Laenu ja Maksete tähtaja ületamisel 21 või enam kalendripäeva, kuid vähem kui 35 kalendripäeva – menetluskulud summas vähemalt 23 (kakskümmend kolm) eurot;</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">3) Laenu ja Maksete tähtaja ületamisel 35 või enam kalendripäeva, kuid vähem kui 65 kalendripäeva – menetluskulud summas vähemalt 42 (nelikümmend kaks) eurot;</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">4) Laenu ja Maksete tähtaja ületamisel 65 või enam kalendripäeva lisandub menetluskulusid 42 eurot iga täiendava 30 kalendripäeva kohta;</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">5) Menetluskulud hagiavalduse või maksekäsu kiirmenetluse avalduse esitamisel kohtusse (toimub kui Laenusaaja Lepingust tulenevad kohustused on Laenuandja ees pikaajaliselt täitmata) summas vähemalt 64 (kuuskümmend neli) eurot, lisaks veel õigusabikulud, riigilõiv ja muud kohtukulud.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">8.5. Menetluskulu käesoleva Lepingu mõistes on tasu, mis katab võlanõude analüüsi kulu, pretensioonide koostamise ja edastamise kulud, Laenuandja töötajate palgakulud jne.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">8.5. Laekunud maksed katavad kohustusi ja kulusid vastavalt lepingu punktis 3.5. kirjeldatule.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><strong>9. Lepingu täitmise tagamine</strong></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">9.1. Laenusaaja vastutab Lepingust tulenevate kohustuste täitmise eest kogu oma olemasoleva ja tulevikus omandatava varaga.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">9.2. Laenusaaja vastutab täielikult ja tingimusteta kõigi tema poolt Laenuandjale esitatud dokumentide õigsuse eest. Laenusaaja on kohustatud hüvitama Laenuandjale kõik kahjud, mis Laenuandja kannab seoses Laenusaaja poolt väärinformatsiooni esitamise või tegeliku olukorra ja tähtsust omava informatsiooni teadliku varjamisega.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">9.3. Laenusaaja annab lepingu sõlmimisega laenuandjale tagasivõtmatu õiguse kontrollida Laenusaaja esitatud andmete tõepärasust ning tema senist maksekäitumist oma partneritelt, riiklikest registritest, krediidiasutustest, maksehäireregistrist jms.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">9.4. Laenuandja teostab laenutaotluse ja laenutaotleja maksevõime analüüsi. Laenuandjal on laenutaotluse ja laenutaotleja saaja maksevõime analüüsi teostamiseks ning Laenu väljastamisega seotud asjaolude tuvastamiseks õigus nõuda laenutaotlejalt tema sissetulekut ja majanduslikku olukorda tõendavate andmete ning dokumentide esitamist.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">9.5. Laenusaaja annab laenuandjale käesoleva lepinguga tagasivõtmatu nõusoleku käesolevast lepingust tulenevate mistahes rahaliste kohustuste täitmata jätmise korral oma isikuandmete (nimi ja isikukood) ning maksehäire tekkimise kuupäeva ning summa edastamisega AS-le Krediidiinfo ning lubab loetletud andmete töötlemist krediidiotsuste langetamise eesmärgil AS Krediidiinfo poolt hallatavas maksehäireregistris (täpsem informatsioon </span><a href="http://www.krediidiinfo.ee/" style="text-decoration:none;"><span style="background-color:transparent; color:rgb(0, 0, 0)">www.krediidiinfo.ee</span></a><span style="background-color:transparent; color:rgb(0, 0, 0)">)</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><strong>10. Lepingu lõppemine</strong></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">10.1. Leping jõustub allakirjutamise hetkest ning lõpeb nõuetekohase täitmisega Laenusaaja ja/või kolmandate isikute poolt.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">10.2. Laenusaajal on õigus lepingust taganeda</span><span style="background-color:transparent; color:rgb(0, 0, 0); font-size:16px"> 14 (</span><span style="background-color:transparent; color:rgb(0, 0, 0)">neljateistkümne</span><span style="background-color:transparent; color:rgb(0, 0, 0); font-size:16px">)</span><span style="background-color:transparent; color:rgb(0, 0, 0)"> päeva jooksul arvates selle sõlmimisest, esitades Laenuandjale selle aja jooksul kirjaliku avalduse ja tagastades laenusumma ning sellelt arvestatud intressid 14 (neljateistkümne) päeva jooksul arvates taganemise avalduse esitamisest. Nimetatud kohustuse mittetäitmisel loetakse, et Laenuandja ei ole lepingust taganenud.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><strong>11. Lepingu erakorraline ülesütlemine</strong></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">11.1. Pooltel on õigus Leping erakorraliselt üles öelda Lepingus ja seaduses toodud alustel.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">11.2. Laenuandjal on õigus Leping üles öelda ja nõuda laenu kohest tagastamist, kui leiab aset kasvõi üks alljärgnevalt loetletud sündmustest:</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="margin-left:36pt; text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">1) Laenusaaja on laenu taotlemisel või muudes laenuga seotud dokumentides andnud Laenuandjale väärinformatsiooni või jätnud esitamata talle teadaolevad Lepingu täitmist mõjutavad andmed;</span></span></p>

<p dir="ltr" style="margin-left:36pt; text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">2) Laenusaaja ei ole nõuetekohaselt tasunud tähtaegselt ja/või ettenähtud summas kasvõi ühte Lepingu alusel tasumisele kuuluvat makset;</span></span></p>

<p dir="ltr" style="margin-left:36pt; text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">3) Laenusaaja ei täida maksekohustusi mõne muu tema poolt võetud laenu osas ning see deklareeritakse ennetähtaegselt tagasimaksmisele kuuluvaks;</span></span></p>

<p dir="ltr" style="margin-left:36pt; text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">4) Laenusaaja ei täida muid Lepingus ja/või selle täitmise tagamiseks sõlmitud lepingus sätestatud tingimusi;</span></span></p>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">5) Ilmneb Laenusaaja võimetus täita Lepingut nõuetekohaselt.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">11.3. Lepingu Laenuandja poolsel erakorralisel ülesütlemisel kohustub Laenusaaja tasuma kõik Laenuandjale võlgnetavad summad 7 (seitsme) kalendripäeva jooksul arvates Lepingu erakorralise ülesütlemise teate kättesaamisest.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">11.4. Laenusaajal on õigus 7 (seitsme) päevase kirjaliku etteteatamisega Leping üles öelda ja tagastada Laenuandjale laenusumma ning tasuda intressid. Laenusumma ennetähtaegsel tagasimaksmisel toimub intresside arvestus krediidi kasutamise eest vastavalt lepingu p 3 ja Lisas 1 sätestatud määradele.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><strong>12. Lõppsätted</strong></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">12.1. Lepingut võib muuta Poolte kokkuleppel kirjalikus vormis või muus kirjalikus taasesitamist võimaldavas vormis. Pooled ei välista lepingu muutmist ka muus (sh suulises) vormis.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">12.2. Pooled lepivad kokku, et Lepingu tingimusteks ei loeta Poolte varasemaid tahteavaldusi, tegusid ega kokkuleppeid, mis ei ole Lepingus otseselt sätestatud.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">12.3. Käesoleva lepingu mistahes sätte võimalik vastuolu seadusega ei mõjuta ülejäänud lepingu sätete kehtivust ega ole aluseks lepingu erakorraliseks ülesütlemiseks. Lepinguga reguleerimata küsimustes juhindutakse Eesti Vabariigi seadusandlusest.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">12.4. Pooled seavad eesmärgiks lahendada Lepingu täitmisel tekkivad vaidlused läbirääkimiste teel. Kokkuleppe mittesaavutamisel lahendatakse vaidlused Eesti Vabariigi seadustega ettenähtud korras kostja elukoha järgses Maakohtus või Pärnu Maakohtu Maksekäsuosakonnas.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">12.5. Lepingu allkirjastamisega kinnitab laenuvõtja, et omab kindlat sissetulekuallikat (töökoht/pension) ning garanteerib laenu tagastamise lepingus toodud tingimustel.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

<p dir="ltr" style="text-align:justify"><span style="font-family:arial,helvetica,sans-serif"><span style="background-color:transparent; color:rgb(0, 0, 0)">12.6. Leping on koostatud kahes identses võrdset juriidilist jõudu omavas eksemplaris, millest üks eksemplar jääb Laenuandjale ja teine Laenusaajale.</span></span></p>
<span style="font-family:arial,helvetica,sans-serif">  </span>

